package br.com.wingoo.model;

public class Dissertativas {

		private int idDissertativa;
		private String conteudo;
		private int id_aluno;
		
		public int getIdDissertativa() {
			return idDissertativa;
		}
		public void setIdDissertativa(int idDissertativa) {
			this.idDissertativa = idDissertativa;
		}
		public String getConteudo() {
			return conteudo;
		}
		public void setConteudo(String conteudo) {
			this.conteudo = conteudo;
		}
		public int getId_aluno() {
			return id_aluno;
		}
		public void setId_aluno(int id_aluno) {
			this.id_aluno = id_aluno;
		}
		
		
}
